#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sums of the two integer;
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two integers
 * @a: first integer
 * @b: second integer
 * Return: substraction of the two integer;
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of two integers
 * @a: first integer
 * @b: second integer
 * Return: product of the two integer;
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: first integer
 * @b: second integer
 * Return: sums of the two integer;
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of the two integer;
 */
int op_mod(int a, int b)
{
	return (a % b);
}

